#include "push_swap.h"

void	ft_putnbr(int n)
{
	n = n - 48;
	if (n < 0)
	{
		if (n == -2147483648)
		{
			ft_putnbr(-21474);
			ft_putnbr(83648);
			return ;
		}
		write (1, "-", 1);
		n = n * -1;
	}
	if (n >= 0 && n <= 9)
	{
		n = n + '0';
		write (1, &n, 1);
	}
	else
	{
		ft_putnbr(n / 10);
		n = n % 10 + '0';
		write (1, &n, 1);
	}
}
